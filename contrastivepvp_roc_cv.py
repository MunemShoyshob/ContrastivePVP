train = pd.read_csv('/content/GTPC-train.csv')
test  = pd.read_csv('/content/GTPC-test.csv')

xtrain = train.drop(['Target'], axis=1).values
ytrain = train['Target'].values

xtest = test.drop(['Target'], axis=1).values
ytest = test['Target'].values

sample_size = xtrain.shape[0]
time_steps  = xtrain.shape[1]
input_dimension = 1

xtrain = xtrain.reshape(sample_size, time_steps, input_dimension)
xtest  = xtest.reshape(xtest.shape[0], time_steps, input_dimension)

n_timesteps = time_steps
n_features  = input_dimension

X = xtrain
