# 5-Fold Cross Validation
def cross_validate_5fold(csv_path, feature_cols, n_splits=5):
    df = pd.read_csv(csv_path)

    X = df[feature_cols].values.astype(np.float32)
    y = df["Target"].values.astype(np.int64)

    skf = StratifiedKFold(n_splits=n_splits, shuffle=True, random_state=SEED)

    fold_metrics = []

    for fold, (train_idx, val_idx) in enumerate(skf.split(X, y), 1):
        print(f"\n================ FOLD {fold} =================")

        # Create fold datasets
        train_df = df.iloc[train_idx]
        val_df   = df.iloc[val_idx]

        train_df.to_csv(f"cv_train_fold{fold}.csv", index=False)
        val_df.to_csv(f"cv_val_fold{fold}.csv", index=False)

        train_ds = FeatureDataset(f"cv_train_fold{fold}.csv", feature_cols)
        val_ds   = FeatureDataset(f"cv_val_fold{fold}.csv", feature_cols)

        train_loader = DataLoader(train_ds, batch_size=BATCH_SIZE, shuffle=True)
        val_loader   = DataLoader(val_ds, batch_size=BATCH_SIZE, shuffle=False)

        # Fresh encoder per fold
        encoder = RNNEncoder(
            input_dim=1,
            rnn_hidden=128,
            bilstm_hidden=32,
            projection_dim=64
        ).to(DEVICE)

        print("→ Contrastive training")
        encoder = train_contrastive(encoder, train_loader)

        # Classifier training
        classifier = Classifier(encoder, embedding_dim=64).to(DEVICE)
        print("→ Classifier training")
        classifier = train_classifier(classifier, train_loader)

        # Evaluation
        print("→ Validation evaluation")
        metrics = evaluate(classifier, val_loader)
        fold_metrics.append(metrics)

    return fold_metrics


