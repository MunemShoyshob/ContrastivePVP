# Aggregate Results
def summarize_cv(results):
    summary = {}
    for key in results[0].keys():
        values = [r[key] for r in results]
        summary[key] = {
            "mean": np.mean(values),
            "std":  np.std(values)
        }
    return summary

cv_summary = summarize_cv(cv_results)

