# 3) Function to extract raw and contrastive features
def extract_features(loader, sample_size):
    all_raw, all_con, all_labels = [], [], []
    with torch.no_grad():
        for x, y in loader:
            x_cpu = x.to(DEVICE).float()
            z = encoder(x_cpu).cpu().numpy()
            all_con.append(z)
            all_raw.append(x_cpu.cpu().numpy().reshape(x.shape[0], -1))
            all_labels.append(y.cpu().numpy())
            if sample_size and sum(len(b) for b in all_labels) >= sample_size:
                break
    X_raw = np.vstack(all_raw)[:sample_size]
    X_con = np.vstack(all_con)[:sample_size]
    y_all = np.concatenate(all_labels)[:sample_size]
    return X_raw, X_con, y_all

# Extract features
