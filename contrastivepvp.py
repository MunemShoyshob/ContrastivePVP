# Settings
DEVICE = torch.device("cuda" if torch.cuda.is_available() else "cpu")
BATCH_SIZE = 128
CONTRASTIVE_EPOCHS = 21
CLASSIFIER_EPOCHS = 34
LR = 2e-3
FEATURE_COL_COUNT = 32
TRAIN_CSV = "/content/biobert-train.csv"
TEST_CSV = "/content/biobert-test.csv"

# Contrastive learning should use ONLY training data
CONTRASTIVE_CSV = TRAIN_CSV
SEED = 42

os.environ["PYTHONHASHSEED"] = str(SEED)
torch.manual_seed(SEED)
np.random.seed(SEED)
random.seed(SEED)

# Dataset
class FeatureDataset(Dataset):
    def __init__(self, csv_file, feature_cols=None, target_col="Target",
                 timesteps=FEATURE_COL_COUNT, feature_dim=1):
        df = pd.read_csv(csv_file)
        if feature_cols is None:
            feature_cols = [c for c in df.columns if c != target_col]
        self.X = df[feature_cols].values.astype(np.float32)
        self.y = df[target_col].values.astype(np.int64)
        self.timesteps = timesteps
        self.feature_dim = feature_dim
        assert self.X.shape[1] == timesteps, f"Expected {timesteps}, got {self.X.shape[1]}"

    def __len__(self):
        return len(self.y)

    def __getitem__(self, idx):
        x = self.X[idx].reshape(self.timesteps, self.feature_dim)
        return torch.from_numpy(x).float(), torch.tensor(self.y[idx], dtype=torch.long)


# Encoder
class RNNEncoder(nn.Module):
    def __init__(self, input_dim=1, rnn_hidden=128, bilstm_hidden=32, projection_dim=64):
        super().__init__()
        self.rnn1 = nn.RNN(input_dim, rnn_hidden, batch_first=True, nonlinearity="tanh")
        self.bilstm = nn.LSTM(rnn_hidden, bilstm_hidden, batch_first=True, bidirectional=True)
        self.fc = nn.Linear(bilstm_hidden * 2, projection_dim)

    def forward(self, x):
        out, _ = self.rnn1(x)
        _, (hn, _) = self.bilstm(out)
        hn_cat = torch.cat([hn[0], hn[1]], dim=1)
        proj = self.fc(hn_cat)
        return F.normalize(proj, dim=1)

# Supervised Contrastive Loss
class SupConLoss(nn.Module):
    def __init__(self, temperature=0.07):
        super().__init__()
        self.temperature = temperature

    def forward(self, features, labels):
        device = features.device
        labels = labels.view(-1, 1)
        mask = torch.eq(labels, labels.T).float().to(device)

        logits = torch.matmul(features, features.T) / self.temperature
        logits_mask = torch.ones_like(mask) - torch.eye(features.size(0), device=device)
        mask = mask * logits_mask

        exp_logits = torch.exp(logits) * logits_mask
        log_prob = logits - torch.log(exp_logits.sum(1, keepdim=True) + 1e-12)

        denom = torch.clamp(mask.sum(1), min=1.0)
        loss = -(mask * log_prob).sum(1) / denom
        return loss.mean()

# Classifier
class Classifier(nn.Module):
    def __init__(self, encoder, embedding_dim=64):
        super().__init__()
        self.encoder = encoder
        self.classifier = nn.Sequential(
            nn.Linear(embedding_dim, 64),
            nn.ReLU(),
            nn.Linear(64, 1),
            nn.Sigmoid()
        )

    def forward(self, x):
        with torch.no_grad():
            z = self.encoder(x)
        return self.classifier(z).squeeze(1)

# Training
def train_contrastive(encoder, loader):
    encoder.train()
    opt = torch.optim.Adam(encoder.parameters(), lr=LR)
    criterion = SupConLoss().to(DEVICE)

    for ep in range(1, CONTRASTIVE_EPOCHS + 1):
        total_loss = 0
        for x, y in loader:
            x, y = x.to(DEVICE), y.to(DEVICE)
            opt.zero_grad()
            loss = criterion(encoder(x), y)
            loss.backward()
            opt.step()
            total_loss += loss.item() * x.size(0)

        print(f"[Contrastive] Epoch {ep}/{CONTRASTIVE_EPOCHS} "
              f"Loss: {total_loss / len(loader.dataset):.6f}")
    return encoder

def train_classifier(classifier, loader):
    classifier.train()
    opt = torch.optim.Adam(classifier.parameters(), lr=LR)
    criterion = nn.BCELoss()

    for ep in range(1, CLASSIFIER_EPOCHS + 1):
        total_loss = 0
        for x, y in loader:
            x, y = x.to(DEVICE), y.float().to(DEVICE)
            opt.zero_grad()
            loss = criterion(classifier(x), y)
            loss.backward()
            opt.step()
            total_loss += loss.item() * x.size(0)

        print(f"[Classifier] Epoch {ep}/{CLASSIFIER_EPOCHS} "
              f"Loss: {total_loss / len(loader.dataset):.6f}")
    return classifier

# Evaluation (UPDATED)
def evaluate(classifier, loader):
    classifier.eval()
    preds, trues = [], []

    with torch.no_grad():
        for x, y in loader:
            x = x.to(DEVICE)
            out = classifier(x).cpu().numpy()
            preds.extend(out.tolist())
            trues.extend(y.numpy().tolist())

    preds_bin = [1 if p >= 0.5 else 0 for p in preds]

    acc = accuracy_score(trues, preds_bin)
    recall = recall_score(trues, preds_bin)
    precision = precision_score(trues, preds_bin)
    specificity = recall_score(trues, preds_bin, pos_label=0)
    f1 = f1_score(trues, preds_bin)
    kappa = cohen_kappa_score(trues, preds_bin)
    mcc = matthews_corrcoef(trues, preds_bin)
    auc = roc_auc_score(trues, preds)

    print(
        f"Accuracy: {acc:.4f} | Sensitivity: {recall:.4f} | "
        f"Specificity: {specificity:.4f}\n"
        f"Precision: {precision:.4f} | F1-score: {f1:.4f} | "
        f"Kappa: {kappa:.4f}\n"
        f"MCC: {mcc:.4f}"
    )

    return {
        "accuracy": acc,
        "recall": recall,
        "specificity": specificity,
        "precision": precision,
        "f1": f1,
        "kappa": kappa,
        "mcc": mcc
    }

# Helpers
def detect_feature_columns(csv_path, target_col="Target"):
    df = pd.read_csv(csv_path, nrows=1)
    return [c for c in df.columns if c != target_col]

# Main
def main():
    feature_cols = detect_feature_columns(CONTRASTIVE_CSV)
    assert len(feature_cols) == FEATURE_COL_COUNT

    contrastive_ds = FeatureDataset(CONTRASTIVE_CSV, feature_cols)
    train_ds = FeatureDataset(TRAIN_CSV, feature_cols)
    test_ds = FeatureDataset(TEST_CSV, feature_cols)

    contrastive_loader = DataLoader(contrastive_ds, BATCH_SIZE, shuffle=True, drop_last=True)
    train_loader = DataLoader(train_ds, BATCH_SIZE, shuffle=True)
    test_loader = DataLoader(test_ds, BATCH_SIZE, shuffle=False)

    encoder = RNNEncoder().to(DEVICE)
    encoder = train_contrastive(encoder, contrastive_loader)
    torch.save(encoder.state_dict(), "encoder_contrastive.pth")

    classifier = Classifier(encoder).to(DEVICE)
    classifier = train_classifier(classifier, train_loader)

    print("\nEvaluating on test set...")
    metrics = evaluate(classifier, test_loader)
    print("Done.")

if __name__ == "__main__":
    main()

