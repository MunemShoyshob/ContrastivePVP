def compute_tsne(X):
    n = len(X)
    perplexity = max(5, min(30, (n - 1) // 3))
    tsne = TSNE(n_components=2, perplexity=perplexity, learning_rate="auto",
                init="pca", random_state=42)
    return tsne.fit_transform(X)

